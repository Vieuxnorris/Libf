#include "libft.h"

ssize_t		ft_strnbr(char *s)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == '\n')
			return ((ssize_t)i);
		i++;
	}
	return (-1);
}
