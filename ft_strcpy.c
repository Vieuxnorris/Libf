#include "libft.h"

char	*ft_strcpy(char *dest, char const *src)
{
	char *tmp;

	tmp = dest;
	while (*src != '\0')
	{
		*tmp = *src;
		tmp++;
		src++;
	}
	*tmp = '\0';
	return (dest);
}
